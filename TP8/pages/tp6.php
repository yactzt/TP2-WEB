<h1 class="page-title">TP 6 &ndash; Galerie d'images</h1>
<p class="page-subtitle">Cr&eacute;ation d'une galerie responsive avec CSS Grid</p>

<div class="content-section">
    <h2>Objectif</h2>
    <p>R&eacute;alisation d'une galerie d'images responsive utilisant CSS Grid pour la disposition et les transitions CSS pour les effets de survol.</p>
</div>

<div class="content-section">
    <h2>Techniques utilis&eacute;es</h2>
    <ul>
        <li><strong>CSS Grid</strong> &ndash; Disposition des images en grille responsive avec <code>auto-fit</code> et <code>minmax()</code></li>
        <li><strong>object-fit</strong> &ndash; Redimensionnement des images sans d&eacute;formation</li>
        <li><strong>Transitions</strong> &ndash; Effets de survol avec <code>transform: scale()</code> et <code>opacity</code></li>
        <li><strong>border-radius</strong> &ndash; Coins arrondis pour un rendu moderne</li>
    </ul>
</div>

<div class="content-section">
    <h2>Structure HTML</h2>
    <p>La galerie est compos&eacute;e d'une section contenant des &eacute;l&eacute;ments <code>&lt;figure&gt;</code>, chacun avec une image <code>&lt;img&gt;</code> et une l&eacute;gende <code>&lt;figcaption&gt;</code>.</p>
    <p>Le fichier CSS externe <code>style.css</code> g&egrave;re la mise en forme et la responsivit&eacute; de la galerie.</p>
</div>
