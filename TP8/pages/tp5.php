<div class="content-section">
    <h2>Exercice 1 &ndash; Mise en page avec Flexbox</h2>
    <p>Utilisation de Flexbox pour cr&eacute;er des mises en page flexibles et responsives. Les propri&eacute;t&eacute;s <code>display: flex</code>, <code>justify-content</code>, <code>align-items</code> et <code>flex-wrap</code> permettent de contr&ocirc;ler la disposition des &eacute;l&eacute;ments.</p>
    <ul>
        <li><code>display: flex</code> &ndash; Active le mode flexbox</li>
        <li><code>justify-content</code> &ndash; Alignement horizontal</li>
        <li><code>align-items</code> &ndash; Alignement vertical</li>
        <li><code>flex-wrap</code> &ndash; Retour &agrave; la ligne automatique</li>
    </ul>
</div>

<div class="content-section">
    <h2>Exercice 2 &ndash; Grid Layout</h2>
    <p>CSS Grid permet de cr&eacute;er des grilles complexes en deux dimensions. Contrairement &agrave; Flexbox qui fonctionne sur un axe, Grid travaille sur les lignes et les colonnes simultan&eacute;ment.</p>
    <ul>
        <li><code>display: grid</code> &ndash; Active le mode grille</li>
        <li><code>grid-template-columns</code> &ndash; D&eacute;finit les colonnes</li>
        <li><code>grid-template-rows</code> &ndash; D&eacute;finit les lignes</li>
        <li><code>gap</code> &ndash; Espacement entre les cellules</li>
    </ul>
</div>

<div class="content-section">
    <h2>Exercice 3 &ndash; Animations CSS</h2>
    <p>Les animations CSS permettent de cr&eacute;er des transitions et des effets visuels sans JavaScript.</p>
    <ul>
        <li><code>transition</code> &ndash; Transition entre deux &eacute;tats</li>
        <li><code>@keyframes</code> &ndash; D&eacute;finition d'animations complexes</li>
        <li><code>animation</code> &ndash; Application d'une animation</li>
        <li><code>transform</code> &ndash; Transformations 2D/3D</li>
    </ul>
</div>
